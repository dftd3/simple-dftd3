elemental function get_vdw_rad_sym(sym) result(rad)

   !> Element symbol
   character(len=*), intent(in) :: sym

   !> Van-der-Waals radius
   real(wp) :: rad

   rad = get_vdw_rad(to_number(sym))

end function get_vdw_rad_sym


!> Get van-der-Waals radius for a pair of element symbols
elemental function get_vdw_rad_pair_sym(sym1, sym2) result(rad)

   !> Element symbol
   character(len=*), intent(in) :: sym1

   !> Element symbol
   character(len=*), intent(in) :: sym2

   !> Van-der-Waals radius
   real(wp) :: rad

   rad = get_vdw_rad(to_number(sym1), to_number(sym2))

end function get_vdw_rad_pair_sym


!> Get van-der-Waals radius for a single atomic number
elemental function get_vdw_rad_num(num) result(rad)

   !> Atomic number
   integer, intent(in) :: num

   !> Van-der-Waals radius
   real(wp) :: rad

   rad = 0.5_wp * get_vdw_rad(num, num)

end function get_vdw_rad_num


!> Get van-der-Waals radius for a pair of atomic numbers
elemental function get_vdw_rad_pair_num(num1, num2) result(rad)

   !> Atomic number
   integer, intent(in) :: num1

   !> Atomic number
   integer, intent(in) :: num2

   !> Van-der-Waals radius
   real(wp) :: rad

   ! Magic number from ratio of van-der-Waals and covalent radii over element 1-94.
   !
   ! L. Trombach, S. Ehlert, S. Grimme, P Schwerdtfeger, J.-M. Mewes, PCCP, 2019.
   ! DOI: 10.1039/c9cp02455g
   real(wp), parameter :: cov2vdw = 1.45_wp

   if (num1 > 0 .and. num1 <= max_elem .and. num2 > 0 .and. num2 <= max_elem) then
      if (num1 > num2) then
         rad = vdwrad(num2 + num1*(num1-1)/2)
      else
         rad = vdwrad(num1 + num2*(num2-1)/2)
      end if
   else
      ! best estimate
      if (num1 > 0 .and. num1 <= max_elem) then
         rad = 0.5_wp * vdwrad(num1*(num1+1)/2) + cov2vdw * get_covalent_rad(num2)
      else if (num2 > 0 .and. num2 <= max_elem) then
         rad = 0.5_wp * vdwrad(num2*(num2+1)/2) + cov2vdw * get_covalent_rad(num1)
      else
         rad = cov2vdw * (get_covalent_rad(num1) + get_covalent_rad(num2))
      end if
   end if

end function get_vdw_rad_pair_num


